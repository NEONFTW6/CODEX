#Question:

#  State whether True or False
# a.	A series object is size mutable.
# b.	A Dataframe object is value mutable

# Answer:

# a. False – A Series object is not size mutable. 
# You cannot change its size (i.e., add or remove elements) after creation. 
# However, its values can be modified. 
# b. True – A DataFrame object is value mutable. 
# You can change the values of a DataFrame after it has been created.


